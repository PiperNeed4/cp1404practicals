# 1. If a value other than a float or integer is entered for either the numerator or denominator variables
# 2. If the value 0 is entered for the denominator
# 3. It is not possible to avoid the possibility of a ZeroDivisionError
try:
    numerator = int(input("Enter the numerator: "))
    denominator = int(input("Enter the denominator: "))
    fraction = numerator / denominator
    print(fraction)
except ValueError:
    print("Numerator and denominator must be valid numbers!")
except ZeroDivisionError:
    print("Cannot divide by zero!")
print("Finished.")
